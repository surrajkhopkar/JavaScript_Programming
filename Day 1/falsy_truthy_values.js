/* 
Falsy Values
JS converts these values to false whenever we try to change the data type to boolean 
1. 0
2. ''
3. null
4. undefined
5. Nan

Truthy Values
All other values other than falsy values are converted to True
*/

console.log(Boolean(0));
console.log(Boolean(''));
console.log(Boolean(null));
console.log(Boolean(undefined));
console.log(Boolean(NaN));

